package models;

public class Address {

	private String dno;
	private String location;
	private int pincode;
	private String landmark;

	public String getDno() {
		return dno;
	}

	public String getLocation() {
		return location;
	}

	public int getPincode() {
		return pincode;
	}

	public String getLandmark() {
		return landmark;
	}

}
